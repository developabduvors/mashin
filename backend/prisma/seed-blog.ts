async function main() {
  let count = 0;
  for (const p of POSTS) {
    await prisma.blogPost.upsert({
      where: { slug: p.slug },
      update: {
        title: p.title,
        excerpt: p.excerpt,
        body: p.body,
        coverUrl: p.coverUrl,
        publishedAt: new Date(p.publishedAt),
      },
      create: {
        title: p.title,
        slug: p.slug,
        excerpt: p.excerpt,
        body: p.body,
        coverUrl: p.coverUrl,
        publishedAt: new Date(p.publishedAt),
      },
    });
    count++;
  }
  console.log(`✓ Blog seed tayyor: ${count} ta maqola upsert qilindi.`);
}

main()
  .catch((e) => {
    console.error("✗ Blog seed xatosi:", e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
